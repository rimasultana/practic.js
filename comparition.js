// == equal to

let x = 5;
console.log(x == 8);

let x1 = 10;
console.log(x1 == 20);

let x3 = 5;
console.log(x3 == 5);
console.log(x3 == "5");


// equal value and equal type

console.log(x === 8);
console.log(x === 5);
console.log(x === '5');

// != not equal

console.log(x != 5);
console.log(x != 10);


// !== not equal and not equal type

console.log(x !== 5);
console.log(x !=='5');
console.log(x !== 8);


// >= greater than or equal to

console.log(x >= 8);
console.log(x >= 5);

// <= less than or equal to

console.log(x <= 5);


